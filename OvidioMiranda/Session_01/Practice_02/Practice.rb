# Arithmetic operators
puts "8 + 9 = #{8 + 9}"
puts "35 - 15 = #{35 - 15}"
puts "3 * 9 = #{ 3 * 9}"
puts "20 / 5 = #{20 / 5}"
puts "22 % 3 = #{22 % 3}"
puts "2 ** 8 = #{2 ** 8}"

# Comparison operators
puts "#{10} == #{5} ? #{10 == 5}"
puts "#{10} != #{5} ? #{10 != 5}"
puts "#{10} > #{5} ? #{10 > 5}"
puts "#{10} < #{5} ? #{10 < 5}"
puts "#{10} >= #{5} ? #{10 >= 5}"
puts "#{10} <= #{5} ? #{10 <= 5}"
puts "#{10} <=> #{5} ? #{10 <=> 5}"
puts "(1...10) === #{5} ? #{(1...10) === 5}"
puts "#{10}.eql?(#{10.0}) ? #{10.equal? 10.0}"
